# Operadores


print('Operadores Aritméticos')
a = 9
b = 4
print(f'  a: {a}')
print(f'  b: {b}')
print()

print(f'  a + b: {a+b}')
print(f'  a - b: {a-b}')
print(f'  a * b: {a*b}')
print(f'  a / b: {a / b}')
print(f'  a // b: {a//b}')
print(f'  a % b: {a%b}')
print(f'  a ** b: {a ** b}')
print('\n')


print('Operadores Relacionales')
a = 13
b = 33
print(f'  a: {a}')
print(f'  b: {b}')
print()

print(f'  a > b: {a > b}')
print(f'  a < b: {a < b}')
print(f'  a == b: {a == b}')
print(f'  a != b: {a != b}')
print(f'  a >= b: {a >= b}')
print(f'  a <= b: {a <= b}')
print('\n')


print(f'Operadores Bit a Bit')
a = 10
b = 4
print(f'  a: {a}')
print(f'  b: {b}')
print()

print(f'  a & b: {a & b}')
print(f'  a | b: {a | b}')
print(f'  ~a: {~a}')
print('\n')


print('Operadores de Asignación')
a = 10
b = a
print(f'  a: {a}')
print(f'  b: {b}')
print()

print(f'  b = a: {b}')

b += a
print(f'  b += a: {b}')

b -= a
print(f'  b -= a: {b}')

b *= a
print('  b *= a: {b}')
print('\n')

print('Operadores Lógicos')
a = True
b = False
print(f'  a: {a}')
print(f'  b: {b}')
  
# Print a and b is False
print(f'  a and b -> {a and b}')
  
# Print a or b is True
print(f'  a or b -> {a or b}')
  
# Print not a is False
print(f'  not a -> {not a}')
print()